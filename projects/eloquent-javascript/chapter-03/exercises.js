////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  if(x === y){
    return 0;
  } else if(x > y){
    return y;
  } else {
    return x;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if(num === 0){
    return true;
  } else if(num === 1){
    return false;
  } else if(num < 0){
    return isEven(num + 2);
  } else {
    return isEven(num - 2);
  }
  }


////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let split = string.split("").reduce(function(acc, current){
   if(current === char){
    acc += 1;
   }
   return acc;
  }, 0);
  return split;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let split = string.split("").reduce(function(acc, current){
    if(string.length === 0){
      return 0;
    } else if(current === "B"){
     acc += 1;
    }
    return acc;
   }, 0);
   return split;
}

